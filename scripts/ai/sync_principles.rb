#!/usr/bin/env ruby
# frozen_string_literal: true

require 'digest'
require 'fileutils'
require 'json'
require 'net/http'
require 'optparse'
require 'shellwords'
require 'tempfile'
require 'uri'
require 'yaml'

class SyncPrinciples
  MANIFEST_PATH = '.ai/principles/sources.yml'
  PRINCIPLES_DIR = '.ai/principles/distilled'
  CLAUDE_SKILL_DIR = '.claude/skills/gitlab-coding-principles'
  CLAUDE_SKILL_PATH = "#{CLAUDE_SKILL_DIR}/SKILL.md".freeze
  REPO_ROOT = File.expand_path('../..', __dir__)
  DEFAULT_PROJECT = 'gitlab-org/gitlab'
  DEFAULT_BRANCH = 'master'

  DISTILLATION_PROMPT_PATH = '.ai/principles/distillation_prompt.md'
  BASELINE_SEPARATOR = '<!-- BASELINE_SECTION_TEMPLATE -->'
  AGENTS_MD_HEADER = '<!-- Auto-generated by scripts/ai/sync_principles.rb — do not edit manually -->'

  GENERATED_PATHS = [
    PRINCIPLES_DIR,
    '.agents/skills/gitlab-coding-principles',
    CLAUDE_SKILL_DIR,
    'AGENTS.md',
    'CLAUDE.md'
  ].freeze

  # Cap concurrent Duo distillations to avoid overwhelming the API with
  # ~23 simultaneous requests during a full re-distillation.
  MAX_CONCURRENT_DISTILLATIONS = 4

  module Color
    CODES = { red: 31, green: 32, yellow: 33, cyan: 36, bold: 1, dim: 2 }.freeze

    module_function

    def colorize(text, *styles)
      return text unless $stdout.tty?

      codes = styles.filter_map { |s| CODES[s] }.join(';')
      "\e[#{codes}m#{text}\e[0m"
    end

    def red(text)
      colorize(text, :red)
    end

    def green(text)
      colorize(text, :green)
    end

    def yellow(text)
      colorize(text, :yellow)
    end

    def cyan(text)
      colorize(text, :cyan)
    end

    def bold(text)
      colorize(text, :bold)
    end

    def dim(text)
      colorize(text, :dim)
    end
  end

  class << self
    attr_accessor :duo_backend, :file_cache
  end

  self.file_cache = {}
  FILE_CACHE_MUTEX = Mutex.new

  def self.run
    options = parse_options

    unless options[:dry_run]
      self.duo_backend = resolve_duo_backend
      unless duo_backend
        abort Color.red('ERROR: No Duo backend available. Set GITLAB_DUO_API_TOKEN or install the `duo` CLI.')
      end
    end

    puts Color.bold("Loading manifest from #{MANIFEST_PATH}...")
    manifest = load_manifest

    puts Color.bold("\nUpdating AGENTS.md context loading section...")
    generate_agents_md_context_loading(manifest)

    puts Color.bold("\nGenerating gitlab-coding-principles skill (.agents/skills/ + .claude/skills/)...")
    generate_principles_skill(manifest)

    puts Color.bold("\nInjecting prerequisite notes into distilled files...")
    inject_prerequisite_notes(manifest)

    # Directory-scoped AGENTS.md generation is disabled: these files benefit neither
    # OpenCode (which uses .agents/skills/) nor Claude Code (which uses .claude/skills/).
    # The generate_agents_md method is retained for potential future tool support.
    # generate_agents_md(manifest)

    puts Color.bold("\nScanning principles for stale SSOT sources...")
    affected = find_affected_principles(manifest, force: options[:force], only: options[:only])

    if affected.empty?
      puts "\n#{Color.green('All principles are up to date.')}"
      return
    end

    puts "\n#{Color.yellow("Affected principles: #{affected.keys.join(', ')}")}"

    if options[:dry_run]
      puts "\n#{Color.cyan('[DRY RUN]')} Would re-distill #{affected.size} principle(s)."
      return
    end

    updated, failed = distill_and_write_principles(manifest, affected, rewrite: options[:rewrite])

    if updated.empty? && failed.empty?
      puts "\n#{Color.dim('No meaningful principle updates needed.')}"
      return
    end

    puts "\n#{Color.green("#{updated.size} principle(s) updated.")}"

    if failed.any?
      abort "\n#{Color.red("ERROR: #{failed.size} principle(s) failed after retries: #{failed.join(', ')}")}"
    end

    if options[:push]
      create_branch_and_mr(updated)
    else
      puts "\n#{Color.cyan('[LOCAL]')} Distillation complete. Pass --push to create a branch and MR."
    end
  end

  def self.distill_and_write_principles(manifest, affected, rewrite: false)
    header = '<!-- Auto-generated from docs.gitlab.com by ' \
      "scripts/ai/sync_principles.rb — do not edit manually -->\n\n"

    # Phase 1: distill in parallel
    results = parallel_distill(affected, rewrite: rewrite)

    # Phase 2: write results sequentially
    failed = []
    updated_principles = {}

    affected.each_key do |name|
      current, updated = results[name]

      if updated.nil?
        failed << name
        next
      end

      updated = reduce_diff_noise(current, updated) if current

      unless meaningful_diff?(current, updated)
        puts "  #{name}: #{Color.dim('no meaningful changes')}"
        next
      end

      config = manifest.dig('principles', name)
      note = prerequisite_note(name, manifest)

      updated = "#{header}#{updated}" unless updated.start_with?('<!-- Auto-generated')
      updated = updated.sub(/^(<!-- Auto-generated.*-->)\n\n*/, "\\1\n\n#{note}") if note
      updated = "#{updated.rstrip}\n\n#{sources_footer(config)}"

      checksum = compute_checksum(config)
      content = <<~CONTENT
        ---
        source_checksum: #{checksum}
        distilled_at_sha: #{distillation_base_sha}
        ---
        #{updated}
      CONTENT
      File.write(File.join(REPO_ROOT, principles_path(name)), content)
      updated_principles[name] = updated
      puts "  #{name}: #{Color.green("updated and written to #{principles_path(name)}")}"
    end

    [updated_principles, failed]
  end

  def self.parallel_distill(affected, rewrite: false)
    mutex = Mutex.new
    results = {}

    affected.each_slice(MAX_CONCURRENT_DISTILLATIONS) do |batch|
      threads = batch.map do |name, info|
        Thread.new do
          current = read_principles_file(name)
          updated = distill_principle(name, info[:config], mutex: mutex, rewrite: rewrite)
          mutex.synchronize { results[name] = [current, updated] }
        end
      end
      threads.each(&:join)
    end

    results
  end

  def self.parse_options
    options = {}
    OptionParser.new do |opts|
      opts.banner = 'Usage: ruby scripts/ai/sync_principles.rb [options]'

      opts.on('--dry-run', 'Show what would be done without making changes') do
        options[:dry_run] = true
      end

      opts.on('--push', 'After distillation, create a branch, commit, push, and open an MR') do
        options[:push] = true
      end

      opts.on('--force', 'Force re-distillation of all principles, ignoring checksums') do
        options[:force] = true
      end

      opts.on('--only NAMES', 'Comma-separated list of principle names to process') do |names|
        options[:only] = names.split(',').map(&:strip)
      end

      opts.on('--rewrite', 'Drop rule 9 (preserve wording) so Duo rewrites all items from scratch') do
        options[:rewrite] = true
      end
    end.parse!

    options
  end

  def self.load_manifest
    path = File.join(REPO_ROOT, MANIFEST_PATH)
    abort Color.red("ERROR: Manifest not found at #{path}") unless File.exist?(path)

    YAML.safe_load_file(path)
  end

  def self.principles_filename(name)
    "#{name}.md"
  end

  def self.principles_path(name)
    File.join(PRINCIPLES_DIR, principles_filename(name))
  end

  def self.load_frontmatter_data
    Dir.glob(File.join(REPO_ROOT, PRINCIPLES_DIR, '*.md')).each_with_object({}) do |path, data|
      name = File.basename(path, '.md')
      frontmatter = extract_frontmatter(File.read(path))
      next unless frontmatter&.key?('source_checksum')

      data[name] = {
        checksum: frontmatter['source_checksum'],
        distilled_at_sha: frontmatter['distilled_at_sha']
      }
    end
  end

  def self.extract_frontmatter(content)
    return unless content.start_with?("---\n")

    _empty, frontmatter, _rest = content.split("---\n", 3)
    YAML.safe_load(frontmatter) if frontmatter
  end

  def self.strip_frontmatter(content)
    return content unless content.start_with?("---\n")

    parts = content.split("---\n", 3)
    parts.size == 3 ? parts[2].lstrip : content
  end

  def self.compute_checksum(config)
    sources = config.fetch('sources', [])
    digest = Digest::SHA256.new

    # Hash routing-relevant manifest fields so changes to description,
    # group, prerequisite, or file_filters invalidate the cached checksum
    # (these affect AGENTS.md / skill routing-table generation).
    %w[description group prerequisite].each do |key|
      digest.update("#{key}=#{config[key].inspect}")
    end
    Array(config['file_filters']).each { |filter| digest.update(filter) }

    baseline_path = config['baseline']
    if baseline_path
      digest.update(baseline_path)
      baseline_content = read_repo_file(baseline_path)
      digest.update(baseline_content) if baseline_content
    end

    sources.each do |source|
      digest.update(source['path'])
      content = read_repo_file(source['path'])
      digest.update(content) if content
    end

    digest.hexdigest[0, 16]
  end

  def self.distillation_base_sha
    @distillation_base_sha ||= begin
      default_branch = ENV.fetch('CI_DEFAULT_BRANCH', DEFAULT_BRANCH)
      `git -C #{REPO_ROOT} merge-base HEAD #{default_branch}`.strip
    end
  end

  def self.build_diff_hint(sha, source_paths)
    ['git', 'diff', "#{sha[0, 12]}..HEAD", '--', *source_paths].shelljoin
  end

  def self.find_affected_principles(manifest, force: false, only: nil)
    frontmatter_data = load_frontmatter_data

    manifest.fetch('principles', {}).each_with_object({}) do |(name, config), affected|
      if only && !only.include?(name) # rubocop:disable Rails/NegateInclude -- standalone script without Rails
        puts "  #{name}: #{Color.dim('skipped (not in --only list)')}"
        next
      end

      current_checksum = compute_checksum(config)
      stored = frontmatter_data[name]
      stored_checksum = stored&.dig(:checksum)

      if !force && current_checksum == stored_checksum
        puts "  #{name}: #{Color.green('up to date')}"
      else
        sources = config.fetch('sources', [])
        affected[name] = { config: config, changed_sources: sources }
        reason = if force
                   'forced'
                 else
                   (stored_checksum ? 'sources or docs changed' : 'no previous checksum')
                 end

        puts "  #{name}: #{Color.yellow('needs update')} (#{reason})"

        distilled_at_sha = stored&.dig(:distilled_at_sha)
        if distilled_at_sha
          source_paths = sources.map { |s| s['path'] }
          baseline_path = config['baseline']
          source_paths.unshift(baseline_path) if baseline_path
          diff_cmd = build_diff_hint(distilled_at_sha, source_paths)
          puts "    To see what changed: #{Color.cyan(diff_cmd)}"
        end
      end
    end
  end

  def self.read_repo_file(path)
    # FILE_CACHE_MUTEX guards the shared file_cache hash against concurrent
    # writes from parallel_distill threads. Values are deterministic per path,
    # but without synchronization two threads can simultaneously miss the cache,
    # both call File.read, and both assign -- wasting I/O unnecessarily.
    FILE_CACHE_MUTEX.synchronize do
      file_cache[path] ||= begin
        full_path = File.join(REPO_ROOT, path)
        if File.exist?(full_path)
          File.read(full_path)
        else
          dir = full_path.sub(%r{(\.md|/)$}, '')
          idx_path = File.join(dir, '_index.md')
          File.read(idx_path) if File.exist?(idx_path)
        end
      end
    end
  end

  def self.read_principles_file(name)
    path = File.join(REPO_ROOT, principles_path(name))
    return unless File.exist?(path)

    strip_frontmatter(File.read(path))
  end

  def self.collect_ssot_docs(config)
    config.fetch('sources', []).filter_map do |source|
      full_path = File.join(REPO_ROOT, source['path'])
      unless File.exist?(full_path) || File.exist?(full_path.sub(%r{(\.md)$}, '/_index.md'))
        raise "SSOT source file not found: #{source['path']} — " \
          'check that the path in sources.yml matches an existing file on the current branch'
      end

      content = read_repo_file(source['path'])
      next unless content

      "### Source: #{source['url']}\nPath: #{source['path']}\n\n#{content}"
    end.join("\n\n---\n\n")
  end

  def self.post_json(url, headers: {}, body: {})
    uri = URI(url)
    http = Net::HTTP.new(uri.host, uri.port)
    http.use_ssl = uri.scheme == 'https'
    http.read_timeout = 120

    request = Net::HTTP::Post.new(uri)
    headers.each { |key, value| request[key] = value }
    request['Content-Type'] = 'application/json'
    request.body = body.to_json

    http.request(request)
  end

  def self.resolve_duo_backend
    token = ENV.fetch('GITLAB_DUO_API_TOKEN', '')
    unless token.empty?
      puts Color.dim('Using Duo REST API backend.')
      return { type: :api, token: token }
    end

    duo_path = ENV.fetch('DUO_CLI_PATH', 'duo')
    if cli_available?(duo_path)
      puts Color.dim("Using Duo CLI backend (#{duo_path}).")
      return { type: :cli, path: duo_path }
    end

    nil
  end

  # Returns true if the given CLI is invocable (responds to `version`).
  # Extracted for testability.
  def self.cli_available?(path)
    system(path, 'version', out: File::NULL, err: File::NULL)
  end

  def self.call_duo(prompt)
    case duo_backend[:type]
    when :api then call_duo_api(prompt)
    when :cli then call_duo_cli(prompt)
    end
  end

  def self.call_duo_api(prompt)
    url = ENV.fetch('GITLAB_DUO_API_URL', 'https://gitlab.com/api/v4/chat/completions')
    response = post_json(url,
      headers: { 'Authorization' => "Bearer #{duo_backend[:token]}" },
      body: { content: prompt, with_clean_history: true })

    unless response.is_a?(Net::HTTPSuccess)
      warn Color.red("ERROR: Duo API returned #{response.code}: #{response.body.slice(0, 200)}")
      return
    end

    response.body
  end

  def self.call_duo_cli(prompt)
    # Pass the prompt directly in --goal instead of writing to a file.
    # The Duo CLI agent cannot read files outside the workspace (Tempfile
    # writes to /var/folders/ on macOS) nor gitignored paths like tmp/.
    # IO.popen with an array bypasses shell ARG_MAX limits, so large
    # prompts are safe.
    err_file = Tempfile.new(%w[duo_stderr .log])
    goal = "Follow the instructions below exactly. Return the complete result.\n\n#{prompt}"
    output = IO.popen(
      [duo_backend[:path], 'run', '--goal', goal, '--workflow-type', 'chat', '--log-level', 'info'],
      err: err_file.path, &:read
    )

    stderr_output = File.read(err_file.path).strip

    unless $?&.success?
      warn Color.yellow("  WARNING: Duo CLI exited with status #{$?&.exitstatus}")
      warn Color.dim("  STDERR (last 500 chars): ...#{stderr_output.slice(-500..)}") unless stderr_output.empty?
    end

    content = parse_duo_cli_output(output)

    unless content || output.empty?
      warn Color.yellow('  WARNING: Could not parse Duo CLI response')
      warn Color.dim("  STDERR (last 500 chars): ...#{stderr_output.slice(-500..)}") unless stderr_output.empty?
    end

    content
  ensure
    err_file&.unlink
  end

  # Parses Duo CLI stdout to extract the final completed-message content.
  # The CLI emits structured log lines of the form:
  #   [RunController] {
  #     "isComplete": true,
  #     "content": "<response>"
  #   }
  # Returns the content string from the last complete message, or nil if none found.
  def self.parse_duo_cli_output(output)
    content = nil
    output.scan(/\[RunController\]\s*(\{.+?\n\s*})/m).each do |match|
      message = JSON.parse(match[0])
      content = message['content'] if message['isComplete']
    rescue JSON::ParserError
      next
    end
    content
  end

  def self.seed_current_agent(name, config, mutex)
    log = ->(msg) { mutex ? mutex.synchronize { puts msg } : puts(msg) }
    existing = read_principles_file(name)

    if existing
      log.call(Color.dim("  #{name}: distilling (existing file found)..."))
      existing
    else
      log.call(Color.yellow("  #{name}: no existing file — regenerating from scratch..."))
      title = config&.dig('description') || name.split('-').map(&:capitalize).join(' ')
      "# #{title} Principles\n\n## Checklist\n"
    end
  end

  def self.distill_principle(name, config, mutex: nil, rewrite: false)
    log = ->(msg) { mutex ? mutex.synchronize { puts msg } : puts(msg) }
    log_warn = ->(msg) { mutex ? mutex.synchronize { warn msg } : warn(msg) }

    current = seed_current_agent(name, config, mutex)

    docs = collect_ssot_docs(config)

    if docs.empty?
      log_warn.call(Color.yellow("  WARNING: No SSOT docs found for #{name}"))
      return
    end

    distillation_prompt, baseline_template = load_distillation_prompt

    baseline_section = ''
    baseline_path = config['baseline']
    if baseline_path
      baseline_content = read_repo_file(baseline_path)
      baseline_section = format(baseline_template, baseline_content: baseline_content) if baseline_content
    end

    if rewrite
      distillation_prompt = distillation_prompt
        .gsub(/^9\..*?(?=^10\.)/m, '')
        .gsub(/^---\nCURRENT AGENT FILE:\n%\{current_agent\}\n/, '')
    end

    prompt = format(distillation_prompt,
      current_agent: current, updated_docs: docs, baseline_section: baseline_section)

    max_retries = 3
    updated = nil
    max_retries.times do |attempt|
      log.call("  Calling Duo for #{name}#{" (retry #{attempt})" if attempt > 0}...")
      result = call_duo(prompt)

      if result&.include?('## Checklist')
        updated = result
        break
      end

      msg = "  WARNING: Duo returned invalid content for #{name} (attempt #{attempt + 1}/#{max_retries})"
      log_warn.call(Color.yellow(msg))

      if result.nil?
        log_warn.call(Color.dim('  Response was nil (Duo CLI returned no parseable content)'))
      else
        preview = result.gsub(/\s+/, ' ').strip.slice(0, 200)
        log_warn.call(Color.dim("  Response preview: #{preview}..."))
      end
    end

    unless updated
      log_warn.call(Color.red("  ERROR: Duo failed after #{max_retries} attempts for #{name}"))
      return
    end

    strip_preamble(updated)
  end

  def self.load_distillation_prompt
    path = File.join(REPO_ROOT, DISTILLATION_PROMPT_PATH)
    content = File.read(path)
    parts = content.split(BASELINE_SEPARATOR, 2)
    abort Color.red("ERROR: #{DISTILLATION_PROMPT_PATH} must contain #{BASELINE_SEPARATOR}") unless parts.size == 2

    [parts[0].strip, parts[1].strip]
  end

  def self.strip_preamble(content)
    content
      .sub(/\A.*?(?=^#\s)/m, '')           # strip everything before first heading
      .sub(/^## Output Format\s*\z/m, '')  # strip empty trailing Output Format sentinel
      .rstrip.then { |s| "#{s}\n" }
  end

  # Reduce diff noise by keeping the old wording when Duo merely rephrased a line.
  # Algorithm:
  #   1. Parse both files into sections keyed by ### heading
  #   2. For sections present in both old and new, compare lines
  #   3. If a new line is a close match to an old line (>= 60% word overlap),
  #      keep the old wording instead
  #   4. Genuinely new lines (no close match) are kept as-is
  #   5. Sections only in new output are kept; sections only in old are dropped
  def self.reduce_diff_noise(old_content, new_content)
    old_sections = parse_sections(old_content)
    new_sections = parse_sections(new_content)

    result_lines = []

    new_sections.each do |heading, new_lines|
      old_lines = old_sections[heading]

      unless old_lines
        # New section - keep entirely
        result_lines << heading if heading
        result_lines.concat(new_lines)
        next
      end

      result_lines << heading if heading
      # Build pool of unmatched old lines for this section
      old_pool = old_lines.dup

      new_lines.each do |new_line|
        best_match, best_score = find_best_match(new_line, old_pool)

        if best_score >= 0.6
          # Close enough - keep old wording to avoid diff noise
          result_lines << best_match
          old_pool.delete_at(old_pool.index(best_match))
        else
          # Genuinely new or substantially changed - keep new
          result_lines << new_line
        end
      end
    end

    result_lines.join("\n").then { |s| "#{s}\n" }
  end

  # Parse content into { heading => [lines] } preserving order.
  # The preamble (before first ###) uses nil as key.
  def self.parse_sections(content)
    sections = {}
    current_heading = nil
    current_lines = []

    content.each_line(chomp: true) do |line|
      if line.start_with?('### ')
        sections[current_heading] = current_lines
        current_heading = line
        current_lines = []
      else
        current_lines << line
      end
    end

    sections[current_heading] = current_lines
    sections
  end

  # Word-level Jaccard similarity between two lines.
  def self.word_similarity(line_a, line_b)
    words_a = line_a.downcase.gsub(/[^a-z0-9_\s]/, '').split
    words_b = line_b.downcase.gsub(/[^a-z0-9_\s]/, '').split
    return 0.0 if words_a.empty? && words_b.empty?

    intersection = words_a & words_b
    union = (words_a | words_b)
    union.empty? ? 0.0 : intersection.size.to_f / union.size
  end

  def self.find_best_match(line, candidates)
    best_match = nil
    best_score = 0.0

    candidates.each do |candidate|
      score = word_similarity(line, candidate)
      next unless score > best_score

      best_score = score
      best_match = candidate
      break if score >= 1.0
    end

    [best_match, best_score]
  end

  def self.meaningful_diff?(current, updated)
    return false if updated.nil?
    return true if current.nil?

    normalize_text(current) != normalize_text(updated)
  end

  def self.normalize_text(text)
    text.strip.lines.map(&:strip).reject(&:empty?)
  end

  def self.dirty_generated_paths
    paths = GENERATED_PATHS.join(' ')
    modified = `git -C #{REPO_ROOT} diff --name-only HEAD -- #{paths} 2>&1`.strip.split("\n")
    untracked = `git -C #{REPO_ROOT} ls-files --others --exclude-standard -- #{paths} 2>&1`.strip.split("\n")
    (modified + untracked).reject(&:empty?).uniq
  end

  def self.create_branch_and_mr(changed_principles)
    dirty = dirty_generated_paths
    if dirty.any?
      abort Color.red(
        "ERROR: --push refused: uncommitted changes in tracked output paths:\n" \
          "#{dirty.map { |p| "  #{p}" }.join("\n")}\n" \
          'Commit, stash, or discard these edits before publishing.'
      )
    end

    timestamp = Time.now.utc.strftime('%Y%m%d-%H%M')
    branch = "docs-sync/principles-#{timestamp}"
    project_id = ENV.fetch('CI_PROJECT_ID', DEFAULT_PROJECT)
    api_token = ENV.fetch('GITLAB_API_TOKEN') { abort Color.red('ERROR: GITLAB_API_TOKEN not set, cannot create MR') }
    default_branch = ENV.fetch('CI_DEFAULT_BRANCH', DEFAULT_BRANCH)

    system('git', '-C', REPO_ROOT, 'checkout', '-b', branch, exception: true)
    system('git', '-C', REPO_ROOT, 'add', '-f', *GENERATED_PATHS, exception: true)

    commit_msg = <<~MSG.chomp
      Update AI development principles from SSOT

      Updated: #{changed_principles.keys.join(', ')}

      This commit was auto-generated by scripts/ai/sync_principles.rb
      based on recent changes to development documentation.
    MSG

    system('git', '-C', REPO_ROOT, 'commit', '-m', commit_msg, exception: true)
    system('git', '-C', REPO_ROOT, 'push', '-u', 'origin', branch, exception: true)

    create_mr(branch, default_branch, project_id, api_token, changed_principles, timestamp)
  rescue StandardError => e
    warn Color.red("ERROR: #{e.message}")

    unless system('git', '-C', REPO_ROOT, 'checkout', default_branch)
      warn Color.yellow("  WARNING: cleanup checkout to #{default_branch} failed; you may still be on #{branch}")
    end

    unless system('git', '-C', REPO_ROOT, 'branch', '-D', branch)
      warn Color.yellow("  WARNING: cleanup deletion of branch #{branch} failed; remove it manually if unwanted")
    end

    raise
  end

  def self.create_mr(branch, default_branch, project_id, api_token, changed_principles, timestamp)
    bullets = changed_principles.keys.map { |name| "- `#{name}`" }.join("\n")
    description = <<~DESC
      ## Summary

      This MR updates AI development principles based on recent changes
      to the development documentation (SSOT).

      ### Updated principles

      #{bullets}

      ### How this works

      A scheduled pipeline detects changes to `doc/development/` files
      listed in `.ai/principles/sources.yml`, then uses the
      GitLab Duo API to distill updated principles for the affected
      domains.

      Please review the checklist changes to ensure they accurately
      reflect the documentation updates.
    DESC

    encoded_project = URI.encode_www_form_component(project_id)
    response = post_json(
      "https://gitlab.com/api/v4/projects/#{encoded_project}/merge_requests",
      headers: { 'PRIVATE-TOKEN' => api_token },
      body: {
        source_branch: branch,
        target_branch: default_branch,
        title: "Update AI development principles from SSOT (#{timestamp})",
        description: description,
        labels: 'ai-agent,documentation',
        remove_source_branch: true
      }
    )

    if response.is_a?(Net::HTTPSuccess)
      puts "\n#{Color.green("MR created: #{JSON.parse(response.body)['web_url']}")}"
    else
      abort Color.red("\nERROR: Failed to create MR: #{response.code} #{response.body.slice(0, 200)}")
    end
  end

  def self.puts_write_summary(written, total, label)
    summary = "Wrote #{written} of #{total} #{label}"
    puts written > 0 ? Color.green(summary) : Color.dim(summary)
  end

  def self.generate_agents_md(manifest)
    # Group directories by the principles that target them
    dir_principles = Hash.new { |h, k| h[k] = [] }

    manifest.fetch('principles', {}).each do |name, config|
      config.fetch('agents_md_dirs', []).each do |dir|
        dir_principles[dir] << name
      end
    end

    written = 0
    dir_principles.each do |dir, names|
      dir_path = File.join(REPO_ROOT, dir)
      next unless Dir.exist?(dir_path)

      agents_path = File.join(dir_path, 'AGENTS.md')
      refs = names.map { |n| principle_ref(n) }.join("\n")
      content = agents_md_content(refs)

      next if File.exist?(agents_path) && File.read(agents_path) == content

      File.write(agents_path, content)
      written += 1
    end

    # Clean up orphaned auto-generated AGENTS.md files
    expected_paths = dir_principles.keys.map { |d| File.join(REPO_ROOT, d, 'AGENTS.md') }.to_set
    Dir.glob(File.join(REPO_ROOT, '**', 'AGENTS.md')).each do |path|
      next if expected_paths.include?(path)

      first_line = File.open(path, &:readline).chomp rescue next # rubocop:disable Style/RescueModifier -- simple fallback for empty files
      File.delete(path) if first_line == AGENTS_MD_HEADER
    end

    puts_write_summary(written, dir_principles.size, 'AGENTS.md file(s) across the repository')
  end

  def self.sources_footer(config)
    sources = config.fetch('sources', [])
    return '' if sources.empty?

    lines = sources.map { |s| "- #{s['path']}" }.join("\n")
    <<~FOOTER
      ## Authoritative sources

      For the full picture, see:

      #{lines}
    FOOTER
  end

  def self.agents_md_content(refs)
    <<~AGENTS_MD
      #{AGENTS_MD_HEADER}
      <!-- markdownlint-disable -->
      <!-- vale off -->

      # Principles

      When working in this directory, follow these principles:

      #{refs}
    AGENTS_MD
  end

  def self.principle_ref(name)
    "@.ai/principles/distilled/#{name}.md"
  end

  # Generates the skill routing table body (used by both AGENTS.md and skill files).
  # Returns a multi-line string with group headers and principle entries.
  def self.build_skill_routing_table(manifest)
    principles = manifest.fetch('principles', {})
    static_entries = manifest.fetch('static_entries', [])

    # Build groups in first-appearance order
    groups = {}
    groups_order = []
    principles.each do |name, config|
      group = config['group'] || 'Other'
      unless groups.key?(group)
        groups[group] = []
        groups_order << group
      end

      groups[group] << { name: name, description: config['description'], prerequisite: config['prerequisite'] == true }
    end

    lines = [
      'Evaluate ALL groups below and load principles from EVERY group that applies to',
      'your task. Most tasks span multiple groups (e.g., a model change may need',
      'Backend, Database, and Testing principles). DO NOT stop after the first group.',
      'When your task involves database queries, scopes, or data access patterns,',
      'ALWAYS load Database principles regardless of which files you are editing.',
      ''
    ]
    groups_order.each do |group|
      lines << "**#{group}:**"
      prerequisites = groups[group].select { |e| e[:prerequisite] }

      groups[group].each do |entry|
        line = "- **#{entry[:description]}**: Read .ai/principles/distilled/#{entry[:name]}.md"
        line += agents_md_prerequisite_suffix(entry, prerequisites, group)
        lines << line
      end

      lines << ''
    end

    # Append static entries (no group header, listed flat)
    static_entries.each do |entry|
      lines << "- **#{entry['description']}**: Read #{entry['path']}"
    end

    lines.join("\n")
  end

  # Generates the gitlab-coding-principles SKILL.md from sources.yml and writes it
  # to both .agents/skills/ (OpenCode) and .claude/skills/ (Claude Code).
  def self.generate_principles_skill(manifest)
    routing_table = build_skill_routing_table(manifest)

    skill_content = <<~SKILL
      ---
      name: gitlab-coding-principles
      description: Load all relevant GitLab development principles before planning or implementing. Evaluates every principle group to ensure cross-domain coverage.
      ---

      # Load Project Principles

      #{routing_table.rstrip}
    SKILL

    written = []
    [
      File.join(REPO_ROOT, '.agents/skills/gitlab-coding-principles/SKILL.md'),
      File.join(REPO_ROOT, CLAUDE_SKILL_PATH)
    ].each do |path|
      next if File.exist?(path) && File.read(path) == skill_content

      FileUtils.mkdir_p(File.dirname(path))
      File.write(path, skill_content)
      written << path.delete_prefix("#{REPO_ROOT}/")
    end

    if written.any?
      puts "  Written skill to: #{written.join(', ')}"
    else
      puts Color.dim('  Skill already up to date')
    end
  end

  # Generates the ### OpenCode Context Loading section in AGENTS.md from sources.yml,
  # grouped by the 'group' field in first-appearance order. Also copies to CLAUDE.md.
  def self.generate_agents_md_context_loading(manifest)
    principles = manifest.fetch('principles', {})
    static_entries = manifest.fetch('static_entries', [])
    routing_table = build_skill_routing_table(manifest)

    generated_header = '<!-- BEGIN GENERATED: scripts/ai/sync_principles.rb — do not edit manually -->'
    generated_footer = '<!-- END GENERATED -->'

    opencode_section = [
      generated_header,
      '### OpenCode',
      '',
      routing_table.rstrip,
      generated_footer
    ].join("\n")

    agents_path = File.join(REPO_ROOT, 'AGENTS.md')
    return unless File.exist?(agents_path)

    content = File.read(agents_path)

    # Replace everything between the generated markers (inclusive)
    updated = content.sub(
      /#{Regexp.escape(generated_header)}\n.*?#{Regexp.escape(generated_footer)}/m,
      opencode_section
    )

    return if updated == content

    File.write(agents_path, updated)

    # Keep CLAUDE.md identical for parity
    claude_path = File.join(REPO_ROOT, 'CLAUDE.md')
    File.write(claude_path, updated)

    puts "  Updated AGENTS.md and CLAUDE.md (#{principles.size} principles, " \
      "#{static_entries.size} static entries)"
  end

  def self.agents_md_prerequisite_suffix(entry, prerequisites, group)
    if entry[:prerequisite]
      " *(load for any #{group.downcase} work)*"
    elsif prerequisites.any?
      also = prerequisites.map { |p| ".ai/principles/distilled/#{p[:name]}.md" }.join(', ')
      " *(also load: #{also})*"
    else
      ''
    end
  end

  # Injects prerequisite blockquotes into existing non-prerequisite distilled files
  # for groups that have a prerequisite member, and cross-group prerequisites.
  # Idempotent - updates existing notes if wording changed.
  def self.inject_prerequisite_notes(manifest)
    principles = manifest.fetch('principles', {})

    principles.each_key do |name|
      note = prerequisite_note(name, manifest)
      next unless note

      path = File.join(REPO_ROOT, principles_path(name))
      next unless File.exist?(path)

      content = File.read(path)

      # Consume the header line plus any blank lines that follow so we can
      # insert exactly: header + blank line + note (which already ends with "\n\n").
      auto_header_pattern = /^(<!-- Auto-generated.*-->)\n\n*/

      if content.include?('> **Prerequisite:**')
        stripped = content.sub(/^> \*\*Prerequisite:\*\*.*\n\n+/, '')
        updated = stripped.sub(auto_header_pattern, "\\1\n\n#{note}")
        next if updated == content

        File.write(path, updated)
        puts "  #{name}: updated prerequisite note"
        next
      end

      updated = content.sub(auto_header_pattern, "\\1\n\n#{note}")
      next if updated == content

      File.write(path, updated)
      puts "  #{name}: injected prerequisite note"
    end
  end

  # Returns a blockquote note to prepend to a non-prerequisite distilled file,
  # listing the prerequisite sibling(s) in the same group that should also be loaded.
  # Returns nil if the principle is itself a prerequisite or has no prerequisite siblings.
  def self.prerequisite_note(name, manifest)
    principles = manifest.fetch('principles', {})
    config = principles[name]
    return if config.nil? || config['prerequisite']

    group = config['group']
    return if group.nil?

    prereqs = principles.select do |n, c|
      n != name && c['group'] == group && c['prerequisite']
    end
    return if prereqs.empty?

    paths = prereqs.keys.map { |n| ".ai/principles/distilled/#{n}.md" }.join(', ')
    note = "> **Prerequisite:** If you haven't already, also read #{paths} - " \
      "it contains foundational rules that apply to all #{group.downcase} work."
    "#{note}\n\n"
  end
